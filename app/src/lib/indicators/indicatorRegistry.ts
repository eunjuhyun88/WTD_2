export type FilterMode = 'all' | IndicatorCategory | 'tier_a' | 'popular';

/** Search + filter indicators */
export function searchIndicators(
  query: string,
  filter: FilterMode = 'all',
  limit = 100,
): IndicatorDef[] {
  const q = query.trim().toLowerCase().replace(/[\s_-]/g, '');

  let pool = INDICATOR_REGISTRY;

  // Category filter
  if (filter === 'tier_a') {
    pool = pool.filter((i) => i.tier === 'A');
  } else if (filter === 'popular') {
    pool = pool.filter((i) => i.popularity >= 4);
  } else if (filter !== 'all') {
    pool = pool.filter((i) => i.category === filter);
  }

  if (!q) {
    return pool.sort((a, b) => b.popularity - a.popularity).slice(0, limit);
  }

  // Fuzzy score
  function score(ind: IndicatorDef): number {
    const id     = ind.id.replace(/[\s_-]/g, '');
    const name   = ind.name.toLowerCase().replace(/[\s_-]/g, '');
    const short  = ind.shortName.toLowerCase().replace(/[\s_-]/g, '');
    const desc   = ind.description.toLowerCase();
    const tags   = ind.tags.join(' ').replace(/[\s_-]/g, '');

    if (id === q || name === q)       return 100;
    if (id.startsWith(q) || name.startsWith(q) || short.startsWith(q)) return 90;
    if (id.includes(q) || name.includes(q) || short.includes(q))       return 80;
    if (tags.includes(q))                                                return 70;
    if (desc.includes(q.substring(0, Math.max(q.length - 1, 2))))       return 50;
    return 0;
  }

  return pool
    .map((i) => ({ i, s: score(i) }))
    .filter(({ s }) => s > 0)
    .sort((a, b) => b.s - a.s || b.i.popularity - a.i.popularity)
    .slice(0, limit)
    .map(({ i }) => i);
}

/** Group by category */
export function groupByCategory(
  items: IndicatorDef[],
): Record<IndicatorCategory, IndicatorDef[]> {
  const result = {} as Record<IndicatorCategory, IndicatorDef[]>;
  for (const cat of CATEGORIES) result[cat.key] = [];
  for (const item of items) result[item.category].push(item);
  return result;
}

